4.ความรับผิดชอบและหน้าที่ของสมาชิก
4.1 เข้าสู่ระบบด้วยตัวเอง
เมื่อท่านลงทะเบียนเสร็จ จะต้องล็อคอินเข้าสู่ระบบด้วยตัวเอง เพื่อทำการกรอกข้อมูลตัวเองให้ครบถ้วน หากต้องการแก้ไขรายละเอียด สามารถแก้ไขได้ตลอดเวลา หากข้อมูลส่วนตัวที่ลงทะเบียนไว้ถูกแก้ไขไม่ตรงตามความเป็นจริง ทางบริษัท FIREROCK ขอสงวนสิทธิ์ในการยุติการเป็นสมาชิกและสิทธิในการใช้บริการของท่าน
4.2 หน้าที่และความรับผิดชอบต่อการใช้บริการของสมาชิก
4.2.1 สมาชิกไม่สามารถใช้บริการดังกล่าวในเชิงพาณิชย์ใดๆได้
4.2.2 นอกจากปฏิบัติตามเงื่อนไขการให้บริการ ท่านยังจะต้องปฏิบัติตามข้อกำหนดและมารยาทการใช้งานอินเตอร์เน็ตระหว่างประเทศ
4.2.3 สมาชิกไม่สามารถใช้บริการนี้เพื่อส่งข้อความหมิ่นประมาท หยาบคาย ก้าวร้าว หรือรูปภาพอนาจาร
4.2.4 สมาชิกยินยอมที่จะเคารพสิทธิในทรัพย์สินทางปัญญา ห้ามตีพิมพ์ข้อความ รูปภาพหรือในรูปแบบใดๆที่ละเมิดทรัพย์สินทางปัญหา
4.3 หน้าที่ดูแลบัญชี
4.3.1 โปรดรับรู้ไว้ว่า ท่านต้องเป็นสมาชิกของเว็บไซต์นี้ก่อนถึงจะเริ่มใช้บริการต่างๆได้ แต่สิทธิทั้งหมด（รวมไปถึงทรัพย์สินทางปัญญา）ยังคงเป็นของบริษัท FIREROCK สมาชิกสามารถใช้บริการตามเงื่อนไขข้อตกลง ไม่สามารถปล่อยเช่า ให้ยืม โอน หรืออนุญาตให้บุคคลที่ 3 ใช้งาน
4.3.2 สมาชิกมีหน้าที่เก็บรักษาข้อมูลบัญชี รหัสผ่าน เพื่อใช้เข้าสู่ระบบดำเนินการร่วมกิจกรรมต่างๆบนเว็บไซต์นี้
4.3.3 หากสมาชิกตรวจพบว่าบัญชีหรือรหัสผ่านถูกคนอื่นแอบใช้งานหรือพบสิ่งผิดปกติที่มีผลต่อความปลอดภัยของข้อมูล โปรดแจ้งให้บริษัท FIREROCK ทราบทันที หากเกิดจากความประมาทของตัวท่านเองส่งผลให้ผู้อื่นสามารถใช้งานบัญชีของท่านได้ ทางบริษัท FIREROCK จะไม่รับผิดชอบใดๆทั้งสิ้น

5. ข้อจำกัดความรับผิดชอบการเติมเงินของสมาชิก
หลังจากที่ท่านเป็นสมาชิกของเว็บ FIREROCK แล้ว เมื่อทำการเติมเงินผ่านเว็บไซต์เพื่อใช้จ่ายในเกมต่างๆของ FIREROCK สำเร็จ ไม่สามารถยกเลิก แก้ไข คืนเงินหรือโอนเงินให้บริการอื่นๆได้ หากท่านทำการเติมเงินผ่านช่องทางอื่นที่ไม่ถูกต้อง ทางบริษัท FIREROCK ขอสงวนสิทธิ์ในการยุติการเป็นสมาชิกและการใช้บริการของท่านทันที เพื่อดำเนินการตรวจสอบทางกฎหมาย

