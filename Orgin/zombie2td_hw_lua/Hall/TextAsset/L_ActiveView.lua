ข้อที่ 10 การใช้งานไอดีกับรหัสผ่าน
หลังจากสมาชิกลงทะเบียนสำเร็จ ไม่ควรนำไอดีกับรหัสผ่านไปบอกผู้อื่นหรือให้คนอื่นยืมใช้ หากเกิดความเสียหาย สมาชิกจะต้องรับผิดชอบความเสียหายที่เกิดเองทั้งหมด
