ในกรณีต่อไปนี้ บริษัท FIREROCK มีสิทธิที่จะระงับหรือยกเลิกทั้งหมดหรือบางส่วนของการให้บริการ ซึ่งอาจจะส่งผลให้ผู้เล่นเกิดความไม่สะดวกหรือความเสียหายต่อการบริการสมาชิกหรือบุคคลใดๆ ทางบริษัท FIREROCK จะไม่รับผิดชอบ
