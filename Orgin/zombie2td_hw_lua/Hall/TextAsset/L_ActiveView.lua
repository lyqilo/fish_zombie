2.การแก้ไขและปรับปรุงเงื่อนไขการให้บริการสมาชิก
บริษัท FIREROCK ขอสงวนสิทธิ์ในการแก้ไขและเพิ่มเติมข้อตกลงของสมาชิก หากมีการแก้ไขหรือเพิ่มเติมข้อตกลง ทางบริษัท FIREROCK จะประกาศให้ทราบทางเว็บไซต์ของบริษัทอย่างเป็นทางการ หากท่านไม่เห็นด้วยกับเนื้อหาที่แก้ไขหรือเพิ่มเติม โปรดอย่าดำเนินการใดๆทั้งสิ้น แต่ถ้าท่านใช้บริการหลังประกาศก็จะถือว่าท่านได้ยอมรับเนื้อหาที่แก้ไขหรือเพิ่มเติมดังกล่าว

