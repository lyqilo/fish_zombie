การตีความและการประยุกต์ใช้ข้อตกลงและเงื่อนไขของการให้บริการ รวมทั้งสมาชิกที่ใช้บริการมีความเกี่ยวข้องกับบริษัท FIREROCK ซึ่งจะตีความตามกฎหมายบังคับใช้ของประเทศสาธารณรัฐประชาชนจีน (ไม่รวมกฎหมายแพ่งของต่างประเทศที่บังคับใช้หรือกฎหมายอื่นที่คล้ายคลึงกัน)

