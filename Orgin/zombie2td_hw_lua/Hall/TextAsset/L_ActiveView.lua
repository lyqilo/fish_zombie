หากเกิดความขัดแย้งต่อสัญญาข้อตกลงการให้บริการ ให้ตกลงกันที่ศาลชั้นต้น กรุงไทเป ประเทศไต้หวัน หากไม่เป็นไปตามข้อตกลงสามารถฟ้องศาลตามพระราชบัญญัติกฎหมายคุ้มครองผู้บริโภคมาตราที่ 47 หรือการประมวลกฎหมายคดีแพ่งบรรทัดที่ 3, 4, 6


สัญญาข้อตกลงการใช้บริการเกมออนไลน์
เพื่อปกป้องข้อมูลสมาชิก（รายละเอียดสมาชิก กรุณากรอกข้อมูลหน้าเว็บลงทะเบียน ซึ่งต่อไปจะเรียกว่า สมาชิก）ของบริษัท FIREROCK ผู้ให้บริการเกมออนไลน์ และกรุณาศึกษารายละเอียดข้อตกลงการใช้บริการเกมออนไลน์ก่อนจะทำการลงทะเบียน เนื่องจากข้อตกลงนี้อาจจะมีการเปลี่ยนแปลงได้ตลอดเวลา เพื่อปกป้องผลประโยชน์ของตัวเอง โปรดศึกษาอย่างละเอียด

ข้อที่ 1 สมัครใช้บริการเกมออนไลน์
เมื่อสมาชิกเข้าใจข้อตกลงแล้ว ให้กดปุ่ม "ยอมรับ" ซึ่งจะถือว่าสมาชิกได้ศึกษาเงื่อนไขข้อตกลงอย่างละเอียดและยินยอมปฏิบัติตามข้อตกลงทั้งหมดที่ได้ระบุไว้ 
การสมัครใช้บริการเกม หากสมาชิกยังไม่บรรลุนิติภาวะจะต้องมีผู้ปกครองอธิบายเงื่อนไขและยินยอมให้สมัครบริการดังกล่าว

ข้อที่ 2 ขอบเขตบังคับใช้ของหนังสือสัญญา
ผู้ให้บริการเกมออนไลน์รวมถึงบริการอื่นๆให้สมาชิก（ซึ่งเรียกสั้นๆว่า บริการ）ซึ่งทั้งสองฝ่ายจะต้องปฏิบัติตามกฎข้อบังคับต่างๆตามที่หนังสือสัญญาได้ระบุไว้

ข้อที่ 3 รายละเอียดของสัญญา
ข้อมูลด้านล่างนี้เป็นเพียงข้อมูลส่วนหนึ่งของสัญญาให้พิจารณา
1.ผู้ให้บริการรับผิดชอบเนื้อหาโฆษณากับประกาศ
2.ตารางอัตราค่าใช้จ่ายกับกฎระเบียบการจัดการเกม
หากรายละเอียดสัญญามีข้อขัดแย้ง โปรดอธิบายผลประโยชน์ให้แก่ผู้บริโภค

ข้อที่ 4 ขอบเขตการบริการ
เป็นหนังสือสัญญาการให้บริการ โดยผู้ให้บริการต้องจัดเตรียมเซิร์ฟเวอร์ การเชื่อมเว็บไซต์เกมผ่านเครือข่ายอินเตอร์เน็ตต่างประเทศ แต่ไม่รวมกับการเชื่อมต่ออินเตอร์เน็ตของสมาชิกและฮาร์ดแวร์ต่างๆที่ใช้เชื่อมต่ออินเตอร์เน็ต

ข้อที่ 5 เข้าสู่ระบบเกม
เมื่อสมาชิกลงทะเบียนเข้าเกม จำเป็นต้องกรอกข้อมูลและหมายเลขบัตรประจำตัวจริงเท่านั้น หากข้อมูลไม่ต้องถูกต้อง สมาชิกจำเป็นต้องรีบแก้ไขให้ถูกต้อง เพื่อป้องกันเหตุสุดวิสัยจะสามารถมีหลักฐานยืนยันตนที่แท้จริงได้ มิฉะนั้นสมาชิกจะต้องรับผิดชอบความเสียหายที่เกิดขึ้นเอง
ถ้าหากสมาชิกกรอกข้อมูลส่วนตัวไม่ตรงตามความจริง หากทีมงานทำการแจ้งให้สมาชิกรีบแก้ไขแต่สมาชิกยังไม่ดำเนินการ ทางทีมงานขอสงวนสิทธิ์ที่จะระงับการให้บริการดังกล่าว โดยไม่เกิน 7 วันทำการ (แต่ไม่ได้ระงับการเข้าเกม และค้นหาประวัติดำเนินการในเกมต่างๆ) 
โดยสมาชิกต้องส่งอีเมล์หรือเขียนจดหมายแจ้งทีมงานให้ยกเลิกสัญญาดังกล่าวภายใน 7 วันหลังเริ่มเข้าเกม โดยสมาชิกไม่จำเป็นต้องอธิบายเหตุผลและรับผิดชอบค่าใช้จ่าย หลังยกเลิกสัญญา สมาชิกสามารถส่งคำขอคืนเงินที่เติมไปแล้วที่เหลือได้

ข้อที่ 6 มาตรฐานการชำระเงิน
เมื่อปรับอัตราค่าใช้จ่าย ทางผู้ให้บริการจะต้องแจ้งรายละเอียดการปรับต่างๆให้สมาชิกทุกคนทราบผ่านทางหน้าเว็บไซต์เกมหลัก หน้าประกาศในเกม และภายในเกมด้วย และจะต้องส่งอีเมล์แจ้งให้สมาชิกทุกคนด้วย
หากอัตราค่าใช้จ่ายที่ปรับใหม่มีค่าสูงกว่าเดิม ผู้ให้บริการจะต้องคิดค่าใช้จ่ายตามราคาเดิมจนกว่าจะถึงเวลาที่ปรับราคาใหม่
ใช้เวลาในการปรับหน่วยเงินในเกมทั้งหมดประมาณ 2 ชม.

ข้อที่ 7 ต้องระบุรายละเอียดของเกม
ผู้ให้บริการจะต้องแจ้งรายละเอียดดังต่อไปนี้ไว้ที่เกมและเว็บไซต์หลักของเกม：
1.ฮาร์ดแวร์ ซอฟต์แวร์ขั้นต่ำที่เหมาะสมกับเกมนี้
2.สิทธิในการขอคืนเงินตามข้อที่ 8 วรรคที่ 1
3.จัดเรตติ้งเกมให้เหมาะสมตามช่วงอายุตามมาตรการจัดอันดับเรตติ้งของเกมซอฟต์แวร์
4.ต้องเตรียมวิธีติดตั้งที่ปลอดภัยให้สมาชิก ไม่ว่าจะฟรีหรือหรือจ่ายเงินก็ตาม

ข้อที่ 8 ผลิตภัณฑ์เกมกับซอฟต์แวร์การคืนเงิน
หากสมาชิกซื้อแพ็คเกจเกมหรือชำระเงินเกี่ยวกับดาวน์โหลดซอฟต์แวร์ภายใน 7 วัน สามารถยื่นเรื่องขอคืนเงินได้เต็มจำนวน
หากฝ่ายบริหารธุรกิจซอฟต์แวร์เกมไม่แก้ไขหรือแก้ไขไม่ได้ ผู้ให้บริการจะต้องรีบดำเนินการคืนเงินให้สมาชิกโดยเร็ว

ข้อที่ 9 อายุสัญญา
หลังจากที่สมาชิกได้ศึกษาเงื่อนไงการสมัครสมาชิกแล้วกด "ยอมรับ" เงื่อนไขพร้อมทั้งลงทะเบียนผ่านหน้าเว็บไซต์เรียบร้อยแล้ว 
